print("Math Functions")
int_to_string = str(10)  # str converts from other data types to string
print(int_to_string)

abs_int = abs(-1)   # Declaring a variable named abs(return absolute value)
print(abs_int)

int_to_float = float(100)   # float(Convert any value to float value)
print(int_to_float)

float_to_int = int(1.23)    # int(Convert any value to integer)
print(float_to_int)
print(int(1.34))

print(max(1, 2, -5, 10, 0))  # max tells us which is the largest
print(min(1, 2, -5, 10, 0))    # min tells us which is the smallest
print(pow(2, 3))    # pow raise the 2 to the power of 3
print(round(51.6))  # round tells use the rounded output
